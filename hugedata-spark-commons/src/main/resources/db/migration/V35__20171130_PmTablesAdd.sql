  `PROJECT_ID` int(8) NOT NULL AUTO_INCREMENT,
  `PROJECT_NAME` varchar(200) NOT NULL,
  `PROJECT_CODE` varchar(200) DEFAULT NULL,
  `PROJECT_ADDR` varchar(200) DEFAULT NULL,
  `PROJECT_AREA` varchar(200) DEFAULT NULL,
  `PROJECT_TYPE` varchar(1000) DEFAULT NULL,
  `PROJECT_CONTENT` varchar(1000) DEFAULT NULL,
  `MAIN_ACTOR` varchar(200) DEFAULT NULL,
  `DUTY_ORG` varchar(200) DEFAULT NULL,
  `DUTY_MAN` varchar(200) DEFAULT NULL,
  `CONTACT_LEAD` varchar(200) DEFAULT NULL,
  `REPO_NAME` varchar(200) DEFAULT NULL,
  `BEGIN_END` varchar(50) DEFAULT NULL,
  `INVEST_TOTAL` double DEFAULT NULL,
  `ACCOUNT_COMMENT` varchar(500) DEFAULT NULL,
  `PROJECT_STATUS` char(1) DEFAULT NULL,
  `INF_COMPANY_ID` int(11) DEFAULT NULL,
